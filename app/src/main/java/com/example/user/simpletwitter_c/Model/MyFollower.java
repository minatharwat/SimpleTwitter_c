public class MyFollower {

    public String fullname;
    public  String profile_url_pic;
    public  String bio;

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getProfile_url_pic() {
        return profile_url_pic;
    }

    public void setProfile_url_pic(String profile_url_pic) {
        this.profile_url_pic = profile_url_pic;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }
}
